# El bucle 'for' recorre cada elemento de una lista uno a uno.
# La variable 'fruta' toma el valor de cada elemento en cada iteración.

frutas = ["manzana", "banana", "cereza"]

for fruta in frutas:
    print(fruta)  # Imprime cada fruta en una línea separada

# Resultado:
# manzana
# banana
# cereza
