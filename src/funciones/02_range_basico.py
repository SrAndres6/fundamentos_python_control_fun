# range(stop) genera una secuencia de números desde 0 hasta stop-1.
# Es la forma más simple de repetir algo un número fijo de veces.

for i in range(5):  # Genera: 0, 1, 2, 3, 4
    print(i)

# Resultado:
# 0
# 1
# 2
# 3
# 4
