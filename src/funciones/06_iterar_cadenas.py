# En Python las cadenas son secuencias de caracteres.
# El bucle for puede recorrerlas letra por letra.

mensaje = "Python"

for letra in mensaje:
    print(letra)  # Imprime cada carácter en una línea

# Resultado:
# P
# y
# t
# h
# o
# n
